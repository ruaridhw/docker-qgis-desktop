#!/bin/bash
set -e

mkdir /build/release
cd /build/release

cmake /build/QGIS \
    -GNinja \
	-DWITH_STAGED_PLUGINS=ON \
	-DCMAKE_INSTALL_PREFIX=/usr \
	-DWITH_GRASS=ON \
	-DSUPPRESS_QT_WARNINGS=ON \
	-DENABLE_TESTS=OFF \
	-DWITH_QSPATIALITE=ON \
	-DWITH_QWTPOLAR=OFF \
	-DWITH_APIDOC=OFF \
	-DWITH_ASTYLE=OFF \
	-DWITH_DESKTOP=ON \
	-DWITH_BINDINGS=ON \
	-DDISABLE_DEPRECATED=ON 

ninja install
